package Praticejava;

public class OperatorsConditional {

	public static void main(String[] args) {
		
		int x=10;
		int y=x>12? 100 : 200;
		System.out.println(y);
		

		int ch =13.24==13.4? 'a': 'b';
		System.out.println(ch);
		
	int marks=55;
	String s = marks>50 ? "pass" :"fail";
		System.out.println(s); 

	}

}
