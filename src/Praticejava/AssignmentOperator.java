package Praticejava;

public class AssignmentOperator {

	public static void main(String[] args) {
		int i=2, j=3, k=4, l=1, m=6;
		
		i+=4;
		System.out.println(i);
		
		j-=5;
		System.out.println(j);
		
		k*=2;
		System.out.println(k); 
		
		l/=2;
		System.out.println(l);
		
		m%=1;
		System.out.println(m);

	}

}
